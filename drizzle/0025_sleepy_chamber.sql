CREATE TABLE `audit_calendar_events` (
	`id` int AUTO_INCREMENT NOT NULL,
	`tenantId` int NOT NULL,
	`locationId` int NOT NULL,
	`auditScheduleId` int,
	`auditTypeId` int NOT NULL,
	`auditTypeName` varchar(255) NOT NULL,
	`scheduledDate` date NOT NULL,
	`status` enum('pending','completed','overdue','cancelled') NOT NULL DEFAULT 'pending',
	`reminderSent` boolean NOT NULL DEFAULT false,
	`reminderSentAt` timestamp,
	`assignedToId` int,
	`assignedToName` varchar(255),
	`completedAt` timestamp,
	`completedById` int,
	`auditInstanceId` int,
	`notes` text,
	`isAutoGenerated` boolean NOT NULL DEFAULT false,
	`createdAt` timestamp NOT NULL DEFAULT (now()),
	`updatedAt` timestamp NOT NULL DEFAULT (now()) ON UPDATE CURRENT_TIMESTAMP,
	CONSTRAINT `audit_calendar_events_id` PRIMARY KEY(`id`)
);
